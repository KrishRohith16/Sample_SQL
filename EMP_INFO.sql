-- CREATING TRANSACTION TABLE : EMP_INFO

CREATE TABLE EMP_INFO(
EMP_ID INT IDENTITY PRIMARY KEY,
EMP_NAME VARCHAR(20),
CITY_ID VARCHAR(10),
DEPT_ID VARCHAR(10),
DESIG_ID VARCHAR(10),
SALARY INT,
FOREIGN KEY(CITY_ID) REFERENCES CITY(CITY_ID),
FOREIGN KEY(DEPT_ID) REFERENCES DEPT(DEPT_ID),
FOREIGN KEY(DESIG_ID) REFERENCES DESIG(DESIG_ID)
);

